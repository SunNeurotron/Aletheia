-- Index on `discovered_at` if time-based analysis of hits is common.
CREATE INDEX IF NOT EXISTS idx_discovery_hits_discovered_at ON discovery_hits (discovered_at DESC);


-- Section 3: Optimizing `discovery_jobs` table
-- This table is smaller, but good indexing is still important.

-- The model `JobDB` already has `index=True` for `id` (primary key) and `status`.
-- A composite index on (status, created_at) might be useful for querying jobs by status over time.
CREATE INDEX IF NOT EXISTS idx_discovery_jobs_status_created_at ON discovery_jobs (status, created_at DESC);


-- Section 4: General PostgreSQL Performance Tuning (DBA Level - Conceptual)
-- These are not SQL commands but general advice.
-- 1. Regular VACUUM and ANALYZE: Ensure autovacuum is properly configured, or run these manually
--    periodically, especially after large data loads/deletions.
--    `VACUUM ANALYZE discovery_hits;`
--    `VACUUM ANALYZE discovery_jobs;`
-- 2. Connection Pooling: Use a connection pooler (like PgBouncer) in front of PostgreSQL,
--    especially for applications with many short-lived connections (FastAPI + Celery can generate many).
--    SQLAlchemy has its own pooling but an external pooler is better for many app instances.
-- 3. Configuration Tuning (`postgresql.conf`):
--    - `shared_buffers`: Increase based on available RAM (e.g., 25% of RAM).
--    - `work_mem`: Increase for complex sorts/joins, per connection.
--    - `effective_cache_size`: Set to a large portion of RAM (e.g., 50-75%).
--    - `maintenance_work_mem`: For VACUUM, CREATE INDEX, etc.
--    - Checkpoint tuning (`checkpoint_segments` or `max_wal_size`, `checkpoint_completion_target`).
-- 4. Hardware: Sufficient RAM, fast SSDs for storage.

-- Section 5: Data Compression and Storage Types (Conceptual)
-- - PostgreSQL 14+ offers ZSTD compression for TOASTed data (large values).
-- - Use appropriate data types: `INTEGER` vs `BIGINT` for a, b, c depending on expected magnitude.
--   `models.py` currently uses `Integer`. If numbers can exceed 2 billion, `BIGINT` (SQLAlchemy's `BigInteger`) is needed.
--   The ABC conjecture involves potentially very large numbers.
-- - Archiving: For extremely large datasets, consider strategies to archive older or less relevant
--   data to cheaper storage, or aggregate it.

