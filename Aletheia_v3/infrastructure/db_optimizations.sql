-- Section 2: Specialized Indexing
-- These indexes can be added to the non-partitioned table created by SQLAlchemy,
-- or to the main partitioned table (and they will propagate to partitions in PG11+).

-- Index for searching hits by job_id and then by quality (useful for fetching results of a specific job)
-- SQLAlchemy model already has index=True for job_id and quality, which should create individual indexes.
-- A composite index might be beneficial too.
CREATE INDEX IF NOT EXISTS idx_discovery_hits_job_id_quality ON discovery_hits (job_id, quality DESC);

