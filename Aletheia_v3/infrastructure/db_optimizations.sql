-- This script contains examples of advanced database optimizations for PostgreSQL
-- that can be applied to handle massive storage of discovery_jobs and discovery_hits.
-- These commands are typically applied by a Database Administrator (DBA)
-- after the initial schema creation by SQLAlchemy's `Base.metadata.create_all()`.

-- Important Note: Applying partitioning changes the table structure.
-- SQLAlchemy's default ORM behavior might need adjustments or specific querying
-- strategies if it's not aware of the partitioning. For basic CRUD, it usually works,
-- but performance benefits of partitioning are best realized with partition-aware queries.

-- Section 1: Table Partitioning for `discovery_hits`
-- Partitioning `discovery_hits` by the `quality` field can significantly improve query
-- performance when filtering or analyzing hits within specific quality ranges.
-- It can also help with data management (e.g., archiving or deleting old low-quality partitions).

-- First, ensure the main `discovery_hits` table is created by SQLAlchemy.
-- Then, it needs to be defined as a PARTITIONED table.
-- If `discovery_hits` already exists and has data, it would need to be migrated.
-- For a new setup, you might create `discovery_hits` as partitioned from the start,
-- which SQLAlchemy's `create_all` doesn't directly support declaratively for all DBs.

-- Assuming `discovery_hits` table exists (created by `Base.metadata.create_all()`).
-- The following is conceptual if table is already populated.
-- For a fresh setup, one might define `discovery_hits` as PARTITION BY RANGE (quality)
-- and then create partitions.

-- Example: If we were to recreate discovery_hits as a partitioned table (manual DDL step):
/*
ALTER TABLE discovery_hits RENAME TO discovery_hits_old;

CREATE TABLE discovery_hits (
    id INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    job_id VARCHAR NOT_NULL,
    a INTEGER NOT NULL,
    b INTEGER NOT NULL,
    c INTEGER NOT NULL,
    quality FLOAT NOT NULL,
    discovered_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id, quality), -- Partition key must be part of the primary key
    FOREIGN KEY(job_id) REFERENCES discovery_jobs (id) ON DELETE CASCADE
) PARTITION BY RANGE (quality);

-- Create partitions for different quality ranges:
-- Note: MAXVALUE can be used for the upper bound of the last partition.
-- Ensure ranges are contiguous and cover all expected values.

CREATE TABLE discovery_hits_q_low PARTITION OF discovery_hits
    FOR VALUES FROM (MINVALUE) TO (1.2); -- Hits with quality < 1.2

CREATE TABLE discovery_hits_q_medium PARTITION OF discovery_hits
    FOR VALUES FROM (1.2) TO (1.4); -- Hits with 1.2 <= quality < 1.4

CREATE TABLE discovery_hits_q_high PARTITION OF discovery_hits
    FOR VALUES FROM (1.4) TO (1.6); -- Hits with 1.4 <= quality < 1.6

CREATE TABLE discovery_hits_q_very_high PARTITION OF discovery_hits
    FOR VALUES FROM (1.6) TO (MAXVALUE); -- Hits with quality >= 1.6

-- Don't forget to create indexes on partitions as well, or on the parent partitioned table (PostgreSQL 11+)
-- CREATE INDEX idx_hits_job_id_q_low ON discovery_hits_q_low (job_id); (Example for older PG)
-- CREATE INDEX idx_hits_quality_q_low ON discovery_hits_q_low (quality DESC);
-- For PG11+, indexes on parent table propagate to partitions.
-- The existing indexes on `discovery_hits` (job_id, quality) would propagate if created on the partitioned table.
